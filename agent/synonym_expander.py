def build_synonym_map():
    """构造 word -> [所有同义词] 的字典 (含自身)"""
    m = {}
    for group in SYNONYM_GROUPS:
        for w in group:
            if w not in m:
                m[w] = list(group)
            else:
                # 合并
                existing = set(m[w])
                for g in group:
                    existing.add(g)
                m[w] = list(existing)
    return m


SYNONYM_MAP = build_synonym_map()


def expand_query_terms(text):
    """对 query 文本, 找出文本中出现的同义词组词, 返回扩展词列表

    例如 query 含 "保单贷款", 返回 ["保单贷款", "保单借款", "申请借款", "贷款", "借款"]
    """
    expanded = set()
    for w, variants in SYNONYM_MAP.items():
        if w in text:
            expanded.update(variants)
    return list(expanded)


def expand_query_text(text):
    """对 query 文本附加同义词作为查询扩展词 (用空格拼接,适合 BM25/vector)"""
    extras = expand_query_terms(text)
    # 不重复添加已在 text 中的词
    new_terms = [w for w in extras if w not in text]
    if new_terms:
        return text + " " + " ".join(new_terms)
    return text


